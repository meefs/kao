    fn reg_phase_view<'a>(&self, t: KaoTheme, r: &'a RegisterFlow) -> Element<'a, Message> {
        match &r.phase {
            RegPhase::Ready { quote } => {
                let price = match quote {
                    Some(q) if q.total == U256::ZERO => "free".to_string(),
                    Some(q) => format!("≈ {} ETH", fmt_eth(q.total)),
                    None => "price unavailable".to_string(),
                };
                let btn_label = if r.registry.is_commit_reveal() {
                    "Register — step 1 of 2 (commit)"
                } else {
                    "Register"
                };
                column![
                    text(price).size(18).color(t.text).font(bold()),
                    Space::new().height(4),
                    text("plus network gas · excess is refunded on-chain")
                        .size(11)
                        .color(t.sub),
                    Space::new().height(16),
                    primary_button(t, btn_label, true).on_press(Message::StartRegister),
                ]
                .into()
            }
            RegPhase::Committing => status_line(t, "Committing… confirm on your device.", t.sub),
            RegPhase::Waiting { since, .. } => {
                let elapsed = crate::names::manage::now_secs().saturating_sub(*since);
                if elapsed > registrar::MAX_COMMITMENT_AGE {
                    return column![
                        status_line(t, "Commitment expired (waited over 24h).", t.down),
                        Space::new().height(10),
                        secondary_button(t, "Start over").on_press(Message::ResetRegister),
                    ]
                    .into();
                }
                let remaining = registrar::MIN_COMMITMENT_AGE.saturating_sub(elapsed);
                if remaining > 0 {
                    column![
                        status_line(
                            t,
                            &format!("Commit confirmed. Waiting {remaining}s before step 2…"),
                            t.sub,
                        ),
                        Space::new().height(6),
                        text("This pause stops anyone front-running your registration.")
                            .size(11)
                            .color(t.sub),
                    ]
                    .into()
                } else {
                    column![
                        status_line(t, "Ready to finish.", t.up),
                        Space::new().height(10),
                        primary_button(t, "Complete registration — step 2 of 2", true)
                            .on_press(Message::CompleteRegister),
                    ]
                    .into()
                }
            }
            RegPhase::Registering => status_line(t, "Registering… confirm on your device.", t.sub),
            RegPhase::Done => column![
                status_line(t, &format!("Registered {} {KAO_AVAILABLE}", r.full), t.up),
                Space::new().height(12),
                primary_button(t, "Back to my names", true).on_press(Message::Back),
            ]
            .into(),
            RegPhase::Failed(e) => column![
                status_line(t, e, t.down),
                Space::new().height(10),
                secondary_button(t, "Back").on_press(Message::ResetRegister),
            ]
            .into(),
        }
    }

