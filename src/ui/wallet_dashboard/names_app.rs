/// Format wei as a short ETH string (up to 5 dp, trailing zeros trimmed).
fn fmt_eth(wei: U256) -> String {
    let s = alloy::primitives::utils::format_ether(wei);
    match s.parse::<f64>() {
        Ok(v) => {
            let formatted = format!("{v:.5}");
            formatted
                .trim_end_matches('0')
                .trim_end_matches('.')
                .to_string()
        }
        Err(_) => s,
    }
}

/// "340 days" / "1 day" / "12 hours" for a remaining-seconds count.
fn fmt_days(secs: u64) -> String {
    let days = secs / 86_400;
    if days >= 1 {
        format!("{days} day{}", if days == 1 { "" } else { "s" })
    } else {
        let hrs = secs / 3_600;
        format!("{hrs} hour{}", if hrs == 1 { "" } else { "s" })
    }
}

