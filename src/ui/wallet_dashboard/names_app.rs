    fn home_view<'a>(&'a self, t: KaoTheme) -> Element<'a, Message> {
        let header = row![
            column![
                screen_title(t, "Names"),
                Space::new().height(4),
                screen_subtitle(t, "Search & register across .eth / .gwei / .wei / .xns"),
            ]
            .width(Length::Fill),
            text(KAO_MASCOT).size(22).color(t.a3).font(bold()),
        ]
        .align_y(Alignment::Center)
        .width(Length::Fill);

        // The big search bar — primary interaction. The input fills; the action
        // is a compact pill so the bar gets the room.
        let search = row![
            text_input(
                "search a name — kitty, vitalik.eth, cow.crops…",
                &self.query
            )
            .on_input(Message::QueryChanged)
            .on_submit(Message::Search)
            .padding(Padding::from([14, 18]))
            .size(16)
            .width(Length::Fill)
            .style(move |_, s| text_input_style(t, s)),
            Space::new().width(10),
            pill_button(
                t,
                if self.searching { "…" } else { "Search" },
                Message::Search
            ),
        ]
        .align_y(Alignment::Center)
        .width(Length::Fill);

        let mut body = column![
            header,
            Space::new().height(16),
            search,
            Space::new().height(10)
        ]
        .width(Length::Fill)
        .max_width(640);

        if let Some((is_err, msg)) = &self.notice {
            body =
                body.push(
                    text(msg.clone())
                        .size(12)
                        .color(if *is_err { t.down } else { t.sub }),
                );
            body = body.push(Space::new().height(8));
        }

        // Search mode begins the moment there's text in the box — the rows below
        // track what's typed now, not the last submitted query.
        let trimmed = self.query.trim();
        let has_query = !trimmed.is_empty();

        // ── "Your names" — reverse-discovered + manually added, filtered live by
        //    the typed text so the search bar searches over them too.
        let filter = has_query.then(|| trimmed.to_ascii_lowercase());
        let owned_rows: Vec<&NameStatus> = self
            .owned
            .iter()
            .filter(|n| match &filter {
                Some(q) => n.full.to_ascii_lowercase().contains(q) || n.label.contains(q),
                None => true,
            })
            .collect();

        body = body.push(section_label(t, "YOUR NAMES"));
        body = body.push(Space::new().height(8));
        if owned_rows.is_empty() {
            let msg = if self.scanning {
                "Looking up names that point to you…"
            } else if has_query {
                "None of your names match that."
            } else {
                "No names point to this account yet. Register one below, or add one you own."
            };
            body = body.push(empty_hint(t, msg));
        } else {
            let mut list = column![].spacing(8).width(Length::Fill);
            for n in &owned_rows {
                // Map the filtered reference back to its real index for Manage.
                let idx = self
                    .owned
                    .iter()
                    .position(|m| m.registry == n.registry && m.label == n.label)
                    .unwrap_or(0);
                list = list.push(self.owned_row(t, n, idx));
            }
            body = body.push(list);
        }

        // Manage controls (refresh + manual add) only when not mid-search.
        if !has_query {
            body = body.push(Space::new().height(10));
            body = body.push(self.add_controls(t));
        }

        // ── "Across namespaces" — the cross-namespace availability rows.
        if has_query {
            body = body.push(Space::new().height(18));
            body = body.push(section_label(t, "ACROSS NAMESPACES"));
            body = body.push(Space::new().height(8));
            if self.pending_search || self.searching {
                // Typing in progress, debounce pending, or a search in flight —
                // show the progress hint, never the previous query's rows (which
                // were cleared on the edit anyway).
                body = body.push(empty_hint(t, "Checking every namespace…"));
            } else {
                let mut list = column![].spacing(8).width(Length::Fill);
                for (i, hit) in self.results.iter().enumerate() {
                    list = list.push(self.result_row(t, hit, i));
                }
                body = body.push(list);
            }
        }

        // Reverse-lookup caveat.
        body = body.push(Space::new().height(16));
        body = body.push(
            text(
                "“Your names” are found by reverse lookup — the name each namespace says points \
                 back to you. Names you hold but haven't set as a reverse record won't appear; \
                 add them by name.",
            )
            .size(10)
            .color(t.sub)
            .font(mono()),
        );

        scroll(t, body.into())
    }

