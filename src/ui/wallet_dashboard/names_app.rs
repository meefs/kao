//! Name-service app — a search-first surface for registering and managing names
//! across ENS (`.eth`), GNS (`.gwei`), WNS (`.wei`) and XNS (`.xns`, `.crops`
//! and any permissionless namespace), surfaced as a tile in the Apps launcher.
//!
//! This pane is a self-contained Elm widget: it owns ephemeral UI state and emits
//! [`Outcome`]s for the coordinator to turn into verified reads / signed
//! transactions, and never touches the network itself.
//!
//! - **Home** — one big search bar. Empty, it lists the names that resolve to the
//!   active account (reverse-lookup discovery, plus manual add). Typed, it checks
//!   the label across every namespace and filters your own names by it.
//! - **Register** — a focused panel for one name: the two-step commit → wait 60s
//!   → reveal flow for the commit-reveal registrars, or XNS's single transaction.
//! - **Manage** — renew / re-point an owned commit-reveal name (XNS names are
//!   permanent + immutable, so they have nothing to manage).
//!
//! The commit/reveal `secret` is generated by the coordinator (which owns the
//! RNG), handed back inside the [`RegisterPlan`] on a successful commit, and
//! replayed verbatim on the reveal — so the two transactions agree by
//! construction.

