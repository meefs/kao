    fn result_row<'a>(&self, t: KaoTheme, hit: &'a SearchHit, idx: usize) -> Element<'a, Message> {
        let badge = registry_badge(t, &hit.registry);
        let title = text(hit.full.clone()).size(15).color(t.text).font(bold());

        let (kao, kao_col, status_line_el, right): (
            &str,
            Color,
            Element<'_, Message>,
            Element<'_, Message>,
        ) = match &hit.status {
            HitStatus::Available { quote } => {
                let price = match quote {
                    Some(q) if q.total == U256::ZERO => "free".to_string(),
                    Some(q) => format!("≈ {} ETH", fmt_eth(q.total)),
                    None => "price unavailable".to_string(),
                };
                let perm = if hit.registry.is_xns() {
                    " · permanent"
                } else {
                    ""
                };
                (
                    KAO_AVAILABLE,
                    t.up,
                    text(format!("available · {price}{perm}"))
                        .size(11)
                        .color(t.up)
                        .font(mono())
                        .into(),
                    pill_button(t, "Register", Message::PickResult(idx)),
                )
            }
            HitStatus::Taken { owner } => {
                let yours = *owner == Some(self.owner) || self.owns(&hit.registry, &hit.label);
                let label = if yours { "you own this" } else { "taken" };
                let col = if yours { t.a2 } else { t.sub };
                (
                    KAO_TAKEN,
                    t.sub,
                    text(label).size(11).color(col).font(mono()).into(),
                    Space::new().width(0).into(),
                )
            }
            HitStatus::Unavailable { reason } => (
                "·",
                t.sub,
                text(reason.clone())
                    .size(11)
                    .color(t.sub)
                    .font(mono())
                    .into(),
                Space::new().width(0).into(),
            ),
        };

        let info = column![
            row![
                badge,
                Space::new().width(8),
                title,
                Space::new().width(8),
                text(kao).size(13).color(kao_col),
            ]
            .align_y(Alignment::Center),
            Space::new().height(3),
            status_line_el,
        ]
        .width(Length::Fill);

        card_row(t, info.into(), right)
    }

